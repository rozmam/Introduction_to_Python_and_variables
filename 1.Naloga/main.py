mood = input("What`s your mood today? ")

if mood == "happy":
    print("It`s great to see you happy!")
elif mood =="nervous":
    print("Take a deep breath 3 times.")
elif mood =="sad":
    print("Cher up, mate")
elif mood =="excited":
    print("It`s nice that you`re excited")
else:
    print("I don`t recognize this mood")